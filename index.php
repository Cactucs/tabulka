    <h1>Periodická tabulka pro tisk</h1> 
    <p>Periodická, neboli Mendělejevova tabulka prvků vás v obchodech pár desítek korun stojí. Odteď už si ji nemusíte
    kupovat. V této tabulce naleznete všechno potřebné, a to <strong>zdarma</strong>. Pro nejlepší výsledek tisku doporučuji stáhnout
    tabulku ve formátu XLS, která sice v programu ukazuje řádky, ale ty po výtisku zmizí. Pokud potřebujete jen
    rychle nahlédnout, mrkněte se na PNG (obrázkovou) verzi.</p> 
