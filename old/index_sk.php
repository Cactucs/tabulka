    <h1>Periodická tabuľka pre tisk</h1> 
     <p>Periodická, alebo Mendělejevova tabuľka prvkov vás stojí v obchodoch pár eur. Odteraz už si ju nemusíte
    kupovať. V tejto tabulke nájdete všetko potrebné, a to <strong>zdarma</strong>. Pre najlepší výsledok tisku doporučujem stiahnuť
    tabuľku vo formáte XLS, ktorá síce v programe ukazuje riadky, ale tie po vytlačení zmiznú. Ak potrebujete len
    rýchlo nahliadnuť, mrknite sa na PNG (obrázkovú) verziu.</p> 
